def facilities_for_state(state: str, facility_type: str | None = None) -> list[dict[str, Any]]:
    """Filter seed catalog by state and optional facility type."""
    code = state.strip().upper()
    results: list[dict[str, Any]] = []
    for row in SEED_FACILITIES:
        if row.get("state", "").upper() != code:
            continue
        if facility_type and row.get("facility_type") != facility_type:
            continue
        results.append(dict(row))
    return results


def all_facility_types() -> set[str]:
    return {str(row.get("facility_type", "")) for row in SEED_FACILITIES}
